package com.entity;

public class ChargeImg {
    private Long chargeImgId;
    private String imgAddr;
    private Long shopChargeId;

    public Long getChargeImgId() {
        return chargeImgId;
    }

    public void setChargeImgId(Long chargeImgId) {
        this.chargeImgId = chargeImgId;
    }

    public String getImgAddr() {
        return imgAddr;
    }

    public void setImgAddr(String imgAddr) {
        this.imgAddr = imgAddr;
    }

    public Long getShopChargeId() {
        return shopChargeId;
    }

    public void setShopChargeId(Long shopChargeId) {
        this.shopChargeId = shopChargeId;
    }



}
